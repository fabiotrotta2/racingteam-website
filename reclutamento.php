La domanda di iscrizione al Team è presente sul sito del {Name} RaciNg TeAm e può essere liberamente scaricata, compilata ed infine inviata alla mail del Team nelle seguenti finestre temporali: <br /><br />
•   Primo anno: 25/09 – 14/10 <br />
•   Anni successivi: 01/06 – 31/06<br /><br />
Nota: Le domande inviate otterranno una risposta di accettazione entro le 72 ore dall’invio del modulo, qualora ciò non accada è possibile rinviarlo. Tuttavia, se l’invio è avvenuto al di fuori del periodo sopraindicato, non verranno prese in considerazione!<br /><br />
Dopo la chiusura del bando avverrà il contatto, tramite mail, del responsabile di riferimento del reparto scelto dal candidato che illustrerà la data ed il luogo sia del test preliminare, sia quella del colloquio conoscitivo.<br /><br />
La modalità:<br /><br />
1.  Dopo la chiusura del bando vi sarà nella data e nel luogo indicato per mail il test attitudinale (70 domande in 90 minuti).<br />
Il test d’ingresso è essenzialmente incentrato su quesiti di logica e su vari aspetti, teorici e pratici, in merito alla progettazione e alla produzione di una vettura da competizione.<br />
2.  A seguire il candidato terrà un colloquio conoscitivo/tecnico con il collegio dei responsabili dei vari reparti dove si analizzano i risultati del test preliminare e gli stimoli del candidato a far parte del team.<br />
3.  Successivamente avverrà una seconda prova puramente tecnica (dopo circa una settimana dal colloquio), dove vi è la verifica della conoscenza del regolamento della competizione, tramite domande a risposta multipla (il regolamento può essere scaricato sul sito della competizione) e la verifica sulle competenze inerenti al reparto per cui si è fatta domanda (Qualora servisse un software in particolare, questo sarà opportunamente fornito con il dovuto anticipo o verrà comunicata la maniera per ottenerlo da sé in modo da avere tempo per esercitarsi).<br />
4.  Dopo tale prova tecnica i candidati lavoreranno insieme per risolvere una problematica di tipo gestionale/tecnico così da valutare la loro predisposizione a lavorare in workgroup.<br />
Dopo tali prove, che non sono vincolanti ai fini della valutazione del singolo studente, ma servono esclusivamente per valutare il livello di preparazione del ragazzo e le sue caratteristiche, il collegio dei capireparto deciderà se sia o meno il caso di far effettuare al candidato l’ultima prova, che considera il candidato già come «recluta» del team.<br />
Qualora invece i responsabili non ritengono opportuno proseguire nella valutazione, il ragazzo potrà ripresentarsi l’anno seguente.<br />
Viceversa quindi, qualora i responsabili ritengano il candidato valido, a quest’ultimo verrà assegnato un periodo di prova da “recluta” del team la cui durata è di massimo 2/3 settimane all’interno delle quali la recluta dovrà svolgere un’ultima prova in cui verranno valutate definitivamente la professionalità, l’efficienza nel portare un lavoro completo a compimento, le competenze tecniche, la determinazione, il grado di organizzazione, la serietà e la motivazione del ragazzo.<br /><br />
A seguito del periodo di prova terminato con giudizio positivo da parte del responsabile, la recluta può essere considerata membro attivo del Team.<br /><br />

