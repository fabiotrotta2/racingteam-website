                    <h1>
                        Formula SAE
                    </h1>
<p align="center" style="font-style: italic; margin: 0 auto; display: block; margin-left: auto;">“La formula SAE è l’allenamento ideale per gli studenti che avranno il compito di disegnare il mondo della produzione e delle gare automobilistiche in futuro.” <br />
(Dott. Jeff Belskus, presidente e amministratore delegato del circuito di Indianapolis)</p><br />

La Formula SAE è una competizione tra studenti universitari, organizzata dalla Society of Automotive Engineers (SAE), che prevede la progettazione e la realizzazione di un’auto da corsa, che viene valutata, attraverso una serie di prove, sia da un punto di vista tecnico, in base alla bontà del design, delle prestazioni e dell’affidabilità, sia da un punto di vista economico e finanziario, considerando la sostenibilità dell’idea sul mercato.<br /><br />
<img src="./img/chisiamo1.jpg" style="margin: 0 auto; display: block; margin-left: auto; height: 250px;" class="img-responsive"><br /><br />
 
La competizione si divide in due grandi macro aree che focalizzano la loro attenzione su aspetti differenti: <br />
a) Prove di tipo statico; <br />
b) Prove di tipo dinamico. <br /><br />

Le prime sono orientate all’analisi del progetto dal punto di vista tecnico-economico attraverso tre tipi di prove: <br /><br />

•   Business Presentation, valutazione dal punto di vista industriale e finanziario<br />
•   Cost Report, valutazione dei costi legati alla realizzazione del progetto<br />
•   Design Report, valutazione dal punto di vista tecnologico e prestazionale<br /><br />

Le seconde hanno l’intento di testare le performance del veicolo attraverso: <br /><br />

•   Acceleration su 75m<br />
•   Skid Pad, per testare la tenuta laterale in curva<br />
•   Autocross, test di giro veloce<br />
•   Endurance/Efficency, in combinata sui 22 km mettono alla prova l’affidabilità e i consumi dell’auto<br /><br />

Il punteggio di gara si ottiene sommando i punti accumulati nelle varie prove.<br /><br />
<p align="center"><img src="./img/chisiamo2.jpg" style="margin: 0 auto; margin-left: auto; margin-right: 200px;; height: 250px; display: inline; text-align:center" class="img-responsive"> 
<img src="./img/chisiamo3.jpg" style="height: 250px; display: inline-block; text-align:center" class="img-responsive"><br /><br /></p>
                       

La Formula SAE Series si è diffusa ormai in tutto il mondo, e vanta attualmente nove eventi annuali ufficiali:<br /><br />

•   Germania Formula Student Germany – HockeheimRing<br />
•   Italia Formula SAE Italy - Autodromo Riccardo Paletti<br />
•   Giappone Formula SAE Japan - Ogasayama Sports Park<br />
•   Regno Unito Formula Student - Silverstone Circuit<br />
•   Brasile Formula SAE Brasil - Campo de Provas da Goodyear<br />
•   Australia Formula SAE Australasia - Victoria University Driver Training Center<br />
•   Stati Uniti Formula SAE VIR (Virginia) - Virginia International Raceway<br />
•   Stati Uniti Formula SAE - Michigan International Speedway<br />
•   Stati Uniti Formula SAE West (California) - Auto Club Speedway<br /><br />


Dal 2013 alla versione “classica” con motore a combustione interna e alla versione ibida si è affiancata la nuova frontiera della competizione: la Formula SAE Electric, dove i Team si sfidano nella realizzazione di una monoposto dotata di Power Unit completamente elettrica con potenza massima di 80 kW.<br /><br />

La competizione, come ben comprensibile, ha un grande valore formativo sui partecipanti, i quali possono trasferire in campo pratico le conoscenze teoriche maturate negli anni di studio, in una maniera più vicina a quella tipica del mondo dell’industria.<br /><br />
<img src="./img/chisiamo4.jpg" style="float:left; margin-right: 10px; margin-bottom: 2px; height: 250px;" class="img-responsive"><br />

Dalla sua nascita la Formula SAE ha visto una crescita esponenziale con l’aumento di interesse da parte di aziende e addetti ai lavori, a tal proposito il prestigio di tale competizione è da desumere dall’elevata considerazione in cui è tenuta da parte delle più grandi aziende internazionali nel campo dell’automotive e non, le quali sponsorizzano sia i team che la competizione stessa. Spesso, infatti, tali aziende si rivolgono ai team di Formula SAE come bacini preferenziali di reclutamento di nuovo personale, proprio grazie alle competenze e l’esperienza acquisita dagli studenti partecipando a questa manifestazione.<br /><br />


A riprova dell’importanza della competizione c’è la presenza, in veste di giudici e commissari, di personaggi di sicuro spicco nel mondo automotive, quali Pat Clarke, Giampaolo Dallara, Giancarlo Minardi e Ross Brawn, ad esempio, che si possono incontrare nei vari paddock durante la manifestazione.<br /><br />
